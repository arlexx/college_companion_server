import mongoose, { Schema, Model } from "mongoose";
import { AdminDocument } from "../types";

const AdminSchema = new Schema({
  firstName: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
});

export default mongoose.model<AdminDocument>("admin", AdminSchema);
